    private void cargarInfo() {
        String[] info = SQLusuarios.getInfoUsuario(id);
        nombre.setText(info[0]);
        usuario.setText(info[1]);
        nombreUsuario = info[1];
        contrasena.setText(info[2]);
        SelectorDeGenero.setSelectedItem(info[3].equals("0")?"Hombre":"Mujer");
    }
