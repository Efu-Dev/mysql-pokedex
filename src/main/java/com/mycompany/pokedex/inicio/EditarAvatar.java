    private void FinalizarRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FinalizarRegistroActionPerformed
        String genero = SelectorDeGenero.getSelectedItem().toString().equals("Hombre") ? "0" : "1";
        if(SQLusuarios.editarUsuario(id,nombre.getText(),usuario.getText(),contrasena.getText(), genero, this.nombreUsuario)){
            JOptionPane.showMessageDialog(this, "¡Usuario modificado!");
            RegresarActionPerformed(null);
        }
        else
            JOptionPane.showMessageDialog(this, "¡Ocurrió un error al modificar su usuario.");
    }//GEN-LAST:event_FinalizarRegistroActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(SQLusuarios.eliminarUsuario(id)){
            JOptionPane.showMessageDialog(this, "Su usuario ha sido eliminado.");
            InicioDeSesion newframe = new InicioDeSesion();
            newframe.setVisible (true);
            this.dispose();
        }
        else
            JOptionPane.showMessageDialog(this, "No se ha podido eliminar su usuario.");
    }//GEN-LAST:event_jButton1ActionPerformed

