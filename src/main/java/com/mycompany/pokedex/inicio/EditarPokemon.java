    private void setSelected(){
        String[] movimientos = SQLusuarios.getMovimientosDe(id,nombrePokemon);
        mov1.setSelectedItem(movimientos[0]);
        mov2.setSelectedItem(movimientos[1]);
        mov3.setSelectedItem(movimientos[2]);
        mov4.setSelectedItem(movimientos[3]);
        
        String[] tipos = SQLusuarios.getTiposDe(id,nombrePokemon);
        tipo1.setSelectedItem(tipos[0]);
        tipo2.setSelectedItem(tipos[1]);
        
        String[] otros = SQLusuarios.getOtrosDe(id,nombrePokemon);
        pokemon1.setText(otros[0]);
        apodo.setText(otros[1]);
        region.setSelectedItem(otros[2]);
        habilidad.setSelectedItem(otros[3]);
        
        if(otros[4].equals("0"))
            macho.setSelected(true);
        else if(otros[4].equals("1"))
            hembra.setSelected(true);
        else
            noTiene.setSelected(true);
        
        oldValues[0] = otros[0];
        oldValues[1] = otros[1];
    }

