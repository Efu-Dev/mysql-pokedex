    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String nombre = pokemon1.getText().trim();
        String apodo = this.apodo.getText().trim();
        String tipo1 = this.tipo1.getSelectedItem().toString();
        String tipo2 = this.tipo2.getSelectedItem().toString();
        String region = this.region.getSelectedItem().toString();
        String habilidad = this.habilidad.getSelectedItem().toString();
        String genero = macho.isSelected() ? "0" : (hembra.isSelected() ? "1" : "2");
        String[] movimientos = {mov1.getSelectedItem().toString(), mov2.getSelectedItem().toString(),
            mov3.getSelectedItem().toString(), mov4.getSelectedItem().toString()};
        
        System.out.println(genero); 
        
        if(nombre.equals(null) || tipo1.equals(tipo2) || tipo1.equals("-") 
                || movimientos[0].equals(movimientos[1])
                || movimientos[1].equals(movimientos[2]) || movimientos[2].equals(movimientos[3])){
            JOptionPane.showMessageDialog(this, "El pok√©mon ingresado es inv√°lido. Revise que:\n"
                    + "1. Ni el nombre ni el apodo est√©n vac√≠os.\n"
                    + "2. Los tipos no sea iguales.\n"
                    + "3. El tipo 1 no sea nulo.\n"
                    + "4. No hayan ataques iguales.");
        }
        else if(SQLusuarios.editarPokemon(nombre, apodo, genero, id, habilidad, region, tipo1, tipo2, movimientos, oldValues)){
            JOptionPane.showMessageDialog(this, "¬°Pok√©mon editado!");
        }
        else{
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error al editar el Pok√©mon.");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

